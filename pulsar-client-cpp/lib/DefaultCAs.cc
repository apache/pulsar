void loadDefaultCertificateAuthorities(boost::asio::ssl::context &context) {
    for (const std::string &ca : CAcerts) {
        context.add_certificate_authority(boost::asio::const_buffer(ca.c_str(), ca.length()));
    }
}

std::string writeDefaultTrustStoreOnFile() {
    std::string path = std::tmpnam(nullptr);

    std::ofstream file(path.c_str());
    for (const std::string &ca : CAcerts) {
        file << ca << std::endl;
    }

    file.close();
    return path;
}

